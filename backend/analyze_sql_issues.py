# 问题 2: JOIN 条件用了错误的列名
# ============================================================
print("""
2. 关键问题：JOIN 条件使用错误的列名格式
   ┌─────────────────────────────────────────────────────────┐
   │ 生成的: ON "departments".id = "employees".department_id │
   │ 预期的: ON departments.id = employees.dept_id          │ 
   └─────────────────────────────────────────────────────────┘
   
   employees 表的外键列叫 dept_id 而非 department_id。
   这是 SQLBuilder 的列名映射问题。
""")

# ============================================================
# 问题 3: "列出所有部门" 却带了 WHERE dept_id = 1
# ============================================================
print("""
3. 重要问题：RLS 过滤过度
   ──────────────────────────────────────────
   "列出所有部门" 生成的 SQL 是:
   SELECT "name" FROM "departments" WHERE "dept_id" = 1 LIMIT 1000
   
   departments 表根本没有 dept_id 列！它只有 id, name, parent_dept_id, ...
   
   RLS 引擎强制注入了 departments 不存在的 "dept_id" 过滤条件。
   这会导致 departments 表上的查询始终报错。
""")

# ============================================================
