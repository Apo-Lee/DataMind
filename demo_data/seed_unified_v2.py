def build_org_hierarchy(employees):
    """从 employees 的 manager_id 构建物化汇报路径.

    对每个员工, 沿 manager_id 向上追溯所有祖先, 记录 (ancestor_id, descendant_id, depth).
    同时包含 (id, id, 0) 自引用, 方便权限计算.
    """
    # 构建索引: employee_id → employee
    emp_map = {e[0]: e for e in employees}

    hierarchy = []  # (ancestor_id, descendant_id, depth)

    for emp in employees:
        eid = emp[0]
        # 自引用 (depth=0)
        hierarchy.append((eid, eid, 0))

        # 向上追溯祖先
        current = emp
        depth = 0
        while current[11] is not None:  # manager_id
            manager_id = current[11]
            manager = emp_map.get(manager_id)
            if manager is None:
                break
            depth += 1
            hierarchy.append((manager_id, eid, depth))
            current = manager

    return hierarchy


