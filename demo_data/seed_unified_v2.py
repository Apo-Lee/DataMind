def verify_consistency():
    """验证跨系统数据一致性"""
    print("\n" + "=" * 50)
    print("  跨系统一致性验证")
    print("=" * 50)

    hr_conn = sqlite3.connect(os.path.join(BASE, "hr_demo.sqlite"))
    hr_conn.row_factory = _dict_factory
    hr_c = hr_conn.cursor()

    # 检查部门一致性
    hr_depts = {row["id"]: row for row in hr_c.execute("SELECT * FROM departments")}
    print(f"  HR 部门数: {len(hr_depts)}")

    # 检查员工
    hr_emps = {row["id"]: row for row in hr_c.execute("SELECT * FROM employees")}
    print(f"  HR 员工数: {len(hr_emps)} (在职: {sum(1 for e in hr_emps.values() if e['status'] in ('在职','试用期'))})")

    # 检查汇报关系完整性
    orphaned = [e for e in hr_emps.values() if e["manager_id"] is not None
                and e["manager_id"] not in hr_emps and e["status"] in ("在职","试用期")]
    if orphaned:
        print(f"  [WARN] 孤儿员工 (manager_id 无效): {len(orphaned)}")
    else:
        print("  [OK] 汇报关系完整")

    # 检查 org_hierarchy
    hier_count = hr_c.execute("SELECT COUNT(*) as cnt FROM org_hierarchy").fetchone()["cnt"]
    print(f"  org_hierarchy 记录数: {hier_count}")

    # 检查部门树完整性
    parent_refs = [d for d in hr_depts.values() if d["parent_dept_id"] is not None
                   and d["parent_dept_id"] not in hr_depts]
    if parent_refs:
        print(f"  [WARN] 孤儿部门 (parent_dept_id 无效): {len(parent_refs)}")
    else:
        print("  [OK] 部门树完整")

    hr_conn.close()

    # 检查各系统的 dept_id 和 employee_id 是否与 HR 一致
    for sys_name, db_file, table_cols in [
        ("CRM",  "crm_demo.sqlite",   [("customers", "owner_id"), ("deals", "owner_id"), ("follow_ups", "employee_id")]),
        ("费控", "finance_demo.sqlite", [("expenses", "employee_id"), ("travel_expenses", "employee_id")]),
        ("ERP",  "erp_demo.sqlite",    [("resources", "employee_id"), ("purchase_orders", "requester_id")]),
    ]:
        conn = sqlite3.connect(os.path.join(BASE, db_file))
        for table, col in table_cols:
            try:
                # 检查外键引用
                ids = conn.execute(f"SELECT DISTINCT {col} FROM {table}").fetchall()
                invalid = [r[0] for r in ids if r[0] not in hr_emps]
                if invalid:
                    print(f"  [WARN] {sys_name}.{table}.{col}: {len(invalid)} 个无效引用 (前5个: {invalid[:5]})")
                else:
                    print(f"  [OK] {sys_name}.{table}.{col}: 全部有效")
            except Exception as e:
                print(f"  - {sys_name}.{table}.{col}: 跳过 ({e})")
        conn.close()

    print("\n  验证完成!")


