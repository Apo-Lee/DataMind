    c.executemany(
        "INSERT INTO employees VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)",
        EMPLOYEES,
    )

    # 插入 org_hierarchy
    hierarchy = build_org_hierarchy(EMPLOYEES)
    c.executemany("INSERT INTO org_hierarchy VALUES (?,?,?)", hierarchy)

    # 生成考勤数据 (近6个月, 工作日)
    active_emp_ids = [e[0] for e in EMPLOYEES if e[5] in ("在职", "试用期")]
    att_id = 1
    # 生成日期范围: 2025-12-15 ~ 2026-06-13 (约180天)
    start_date = datetime(2025, 12, 15)
    end_date = datetime(2026, 6, 13)
    day_count = (end_date - start_date).days
    for day_offset in range(day_count):
        date = (start_date + timedelta(days=day_offset))
        date_str = date.strftime("%Y-%m-%d")
        weekday = date.weekday()  # 0=Monday, 6=Sunday
        if weekday >= 5:  # 周末跳过
            continue
        for emp_id in active_emp_ids:
            # 确定性伪随机, 基于日期+员工
            r = (day_offset * 13 + emp_id * 7 + weekday * 3) % 100

            # 周一迟到概率更高, 周五请假概率更高
            if r < 75:
                status, check_in, check_out = "出勤", "08:55", "18:05"
            elif r < 82:
                status, check_in, check_out = "出勤", "09:05", "18:10"
            elif r < 86 and weekday == 0:  # 周一迟到
                status, check_in, check_out = "迟到", f"09:{15+(emp_id*7)%45:02d}", "18:10"
            elif r < 90:
                status, check_in, check_out = "迟到", "09:25", "18:10"
            elif r < 95:
                status, check_in, check_out = "请假", "", ""
            elif r < 97 and weekday == 4:  # 周五请假
                status, check_in, check_out = "请假", "", ""
            else:
                status, check_in, check_out = "缺勤", "", ""

            c.execute("INSERT INTO attendance VALUES (?,?,?,?,?,?)",
                      (att_id, emp_id, date_str, check_in, check_out, status))
            att_id += 1

    conn.commit()
    conn.close()
    active_count = len(active_emp_ids)
    print(f"HR 数据库: {db_path}")
    print(f"  {len(DEPT_TREE)} 部门 (含层级), {TOTAL_EMPLOYEES} 员工 ({active_count} 在职), "
          f"{len(hierarchy)} 汇报路径, {att_id-1} 考勤记录")
    return db_path


