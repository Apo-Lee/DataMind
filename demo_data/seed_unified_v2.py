def create_finance_database():
    """创建费控系统数据库"""
    db_path = os.path.join(BASE, "finance_demo.sqlite")
    if os.path.exists(db_path):
        os.remove(db_path)
    conn = sqlite3.connect(db_path)
    c = conn.cursor()

    c.execute("""CREATE TABLE budgets (
        id INTEGER PRIMARY KEY,
        dept_id INTEGER,
        year INTEGER,
        quarter INTEGER,
        amount REAL,
        used REAL,
        remaining_adj REAL,
        category TEXT,
        budget_type TEXT,
        approver_id INTEGER
    )""")

    c.execute("""CREATE TABLE expenses (
        id INTEGER PRIMARY KEY,
        dept_id INTEGER,
        employee_id INTEGER,
        category TEXT,
        amount REAL,
        date TEXT,
        description TEXT,
        status TEXT,
        approver_id INTEGER,
        expense_type TEXT,
        project_code TEXT
    )""")

    c.execute("""CREATE TABLE travel_expenses (
        id INTEGER PRIMARY KEY,
        expense_id INTEGER,
        employee_id INTEGER,
        departure_date TEXT,
        return_date TEXT,
        destination TEXT,
        purpose TEXT,
        transport_fee REAL,
        hotel_fee REAL,
        meal_fee REAL,
        other_fee REAL
    )""")

    c.execute("""CREATE TABLE cost_centers (
        id INTEGER PRIMARY KEY,
        dept_id INTEGER,
        name TEXT,
        budget_total REAL,
        budget_remaining REAL,
        fiscal_year INTEGER
    )""")

    # ---- 预算 (每季度每部门) ----
    budget_categories = ["人力成本", "办公费用", "营销费用", "设备采购", "差旅费", "培训费", "IT服务", "招待费"]
    budget_types = ["年度预算", "季度预算", "专项预算"]
    approver_ids = [37, 42]  # 赵刚(财务总监), 彭博(财务分析)
    dept_ids = list(DEPT_TREE.keys())

    bid = 1
    for dept_id in dept_ids:
        dept_budget = DEPT_TREE[dept_id][3]  # budget字段
        for q in range(1, 5):
            for ci, cat in enumerate(budget_categories):
                if (dept_id + q + ci) % 3 != 0:
                    continue  # 每个部门每季度约2-3个类别
                # 不同类别的预算比例不同
                cat_ratio = {
                    "人力成本": (0.25, 0.45), "办公费用": (0.05, 0.12),
                    "营销费用": (0.10, 0.25), "设备采购": (0.05, 0.15),
                    "差旅费": (0.05, 0.10), "培训费": (0.02, 0.06),
                    "IT服务": (0.03, 0.10), "招待费": (0.02, 0.05),
                }.get(cat, (0.03, 0.08))
                ratio = cat_ratio[0] + ((dept_id * 7 + q * 13) % 100) / 100.0 * (cat_ratio[1] - cat_ratio[0])
                amount = round(dept_budget * ratio / 4, 2)
                used = round(amount * (0.3 + q * 0.15 + ((dept_id * 11) % 20) * 0.02), 2)
                rem_adj = round(amount - used, 2)
                bt = budget_types[(dept_id + q) % 3]
                c.execute("INSERT INTO budgets VALUES (?,?,?,?,?,?,?,?,?,?)",
                          (bid, dept_id, 2026, q, amount, used, rem_adj, cat, bt,
                           approver_ids[bid % 2]))
                bid += 1

    # ---- 费用报销 (3000) ----
    expense_cats = ["差旅", "办公", "招待", "IT设备", "培训", "营销", "物业", "咨询", "快递", "通讯"]
    expense_types = ["报销", "借款", "直接支出"]
    statuses = (["已审批"]*2000) + (["待审批"]*600) + (["驳回"]*300) + (["已支付"]*100)
    project_codes = [f"PRJ-{i:03d}" for i in range(1, 21)] + [""] * 20

    all_emp_ids = [e[0] for e in EMPLOYEES if e[5] in ("在职", "试用期")]

    for i in range(1, 3001):
        dept_id = dept_ids[(i * 11) % len(dept_ids)]
        emp_id = all_emp_ids[(i * 17) % len(all_emp_ids)]
        # 费用优先使用员工所在部门, 但有时跨部门报销
        emp_dept = next((e[2] for e in EMPLOYEES if e[0] == emp_id), dept_id)
        actual_dept = emp_dept if (i % 4) != 0 else dept_id
        cat = expense_cats[(i * 13) % len(expense_cats)]
        base = {"差旅": 3000, "办公": 200, "招待": 1500, "IT设备": 5000, "培训": 2000,
                "营销": 5000, "物业": 8000, "咨询": 20000, "快递": 50, "通讯": 200}.get(cat, 500)
        amount = round(base * (0.3 + ((i * 370) % 1000) / 1000.0), 2)
        date = (datetime(2026, 6, 13) - timedelta(days=(i * 5) % 365)).strftime("%Y-%m-%d")
        status = statuses[(i * 19) % len(statuses)]
        etype = expense_types[(i * 7) % 3]
        proj = project_codes[(i * 5) % len(project_codes)] if cat in ("营销", "IT设备") else ""

        c.execute("INSERT INTO expenses VALUES (?,?,?,?,?,?,?,?,?,?,?)",
                  (i, actual_dept, emp_id, cat, amount, date,
                   f"{cat}费用-{i:04d}", status, approver_ids[i % 2], etype, proj))

    # ---- 差旅明细 (600) ----
    # 从费用中筛选出差旅/招待类, 生成差旅明细
    destinations = [
        ("北京", "客户拜访"), ("上海", "行业会议"), ("广州", "项目交付"),
        ("深圳", "供应商考察"), ("杭州", "技术交流"), ("成都", "区域巡检"),
        ("武汉", "培训"), ("西安", "合作伙伴拜访"),
    ]
    travel_count = 0
    for i in range(1, 3001):
        if travel_count >= 600:
            break
        if (i * 13) % 5 != 0:  # 约20%的费用关联差旅明细
            continue
        travel_count += 1
        emp_id = all_emp_ids[travel_count % len(all_emp_ids)]
        dest, purpose = destinations[travel_count % len(destinations)]
        days = 2 + (travel_count % 8)
        dep_date = (datetime(2026, 6, 13) - timedelta(days=travel_count * 3)).strftime("%Y-%m-%d")
        ret_date = (datetime(2026, 6, 13) - timedelta(days=travel_count * 3 - days)).strftime("%Y-%m-%d")
        transport = round(800 + travel_count * 230 % 3500, 2)
        hotel = round(300 * days + travel_count * 70 % 600, 2)
        meal = round(100 * days + travel_count * 30 % 200, 2)
        other = round(travel_count * 40 % 300, 2)

        c.execute("INSERT INTO travel_expenses VALUES (?,?,?,?,?,?,?,?,?,?,?)",
                  (travel_count, i, emp_id, dep_date, ret_date, dest, purpose,
                   transport, hotel, meal, other))

    # ---- 成本中心 (每个部门一个) ----
    for ci, dept_id in enumerate(dept_ids, 1):
        dept_name = DEPT_TREE[dept_id][0]
        total = DEPT_TREE[dept_id][3]
        remaining = round(total * (0.2 + (ci * 13 % 80) / 100.0), 2)
        c.execute("INSERT INTO cost_centers VALUES (?,?,?,?,?,?)",
                  (ci, dept_id, f"{dept_name}-成本中心", total, remaining, 2026))

    conn.commit()
    conn.close()
    print(f"费控数据库: {db_path}")
    print(f"  {bid-1} 预算项, 3000 费用, 600 差旅明细, {len(dept_ids)} 成本中心")
    return db_path


